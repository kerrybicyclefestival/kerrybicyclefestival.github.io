 Have a rummage in the shed and pull out that Triumph 20, because we need you for the <a href="index.php?p=family_cycle">Family Cycle.</a><br /></p>

<p>The Inaugural Kerry Bicycle Festival is taking place from the 17th to the 20th of April. That's a Thursday to a Sunday. During this time we will have events ranging from bicycle clowns to bicycle films to bicycle jazz musicians. On the Saturday we are inviting everyone to pull those bikes out of the sheds as we take to the streets of Tralee for our first ever Family Cycle. On the Sunday we're going to cycle to Ballyseede for a picnic. Inspired by the success of Dublin Bicycle Festival which took place in July 2006, we have decided to celebrate the tubular nag down in the Kingdom!  </p>

