<h2>Festival Appeals to local Cyclists </h2>

<p><strong>FOR IMMEDIATE RELEASE:</strong><br/><br/>

<strong>FESTIVAL APPEALS TO LOCAL CYCLISTS</strong><br/>
Tralee, Co. Kerry April 14th 2008<br/>
Many cyclists got a very pleasant surprise this week when they returned to their trusty bicycles only to find a bright red bell had been attached in their absence. They were not completely left in the dark however. Their mysterious benefactors left a rather large clue. Attached to every red bell was a programme for the forthcoming Kerry Bicycle Festival. The red bells are part of Peal, a community bicycle project initiated by Michael Kelly that is to be undertaken in the week preceding the Kerry Bicycle Festival. The project involves the placing of 100 bells on 100 bikes around Tralee. The distinctive look of the bells will allow cyclists identify each other as fellow unwitting members of this project. When interviewed, Mike Kelly said, 'We hope to bring the sound of bicycle bells back into the streets of Tralee. Not only is it sociable and fun but it's safer too.'<br/><br/>

The festival officially kicks off on Thursday night with a bicycle-inspired gig in Clubheadbangbang in the Greyhound Bar on Pembroke street. Friday night sees a public discussion take place in the Samhlaiocht Gallery on Castle Street. This discussion is open to all and will be chaired by the Mayor of Tralee, Miriam McGillycuddy. Local bike-related issues will be discussed including the possibility of opening a Fenit to Tralee and a Ballyroe to Tralee cycleway on the land currently occupied by disused railway line. The state of Tralee's cycle lanes will also be discussed. This discussion will be followed by a viewing of bicycled-related short movies.<br/><br/>

Saturday sees a family cycle to Blennerville followed by general bicycle festivities in Tralee Town Square including the Fanzini brothers and Paddy Waters - the bicycle clown. There will be bicycle elves at the ready to oil your chain and the opportunity to get a bicycle portrait taken in the mobile bicycle studio. The Irish premier of a composition scored for 12 bicycles by avant garde composer Godfried-Willem Raes called 2nd Symphony for Singing Bicyles will also be performed. A bicycle powered mobile phone charger will be at the ready if you run dry and a broken tandem found dumped in Tralee town has been carefully brought back to life for the day that's in it.<br/><br/>

One of the highlights of the festival will be a 'Family Cycle' on Saturday Afternoon, where we hope to get as many people as possible on to their bikes, the cycle will be fully supervised, and will have a 'Cycle Garda' Escort. All are welcome on this event, the meeting point is 12 noon at the Ashe Memorial Hall, and the cycle pace will be leisurely. <br/><br/>

The festival will be brought to a close on Sunday afternoon with a bicycle picnic in Ballyseede Wood.<br/><br/>

<strong>ABOUT KERRY BICYCLE FESTIVAL</strong><br/>
Kerry Bicycle Festival is a four day free festival which brings together a selection of diverse events which aim to celebrate the bicycle. This festival encompasses musical, artistic and outdoor events all of which are free and will take place within the environs of Tralee town. This festival was established in 2008 in order to raise the profile of cycling culture by making it more accessible and attractive and to promote cycling as an environmentally sustainable and cost effective mode of transport.<br/>
