<h2>Mayor of Tralee launches inaugural </h2>

  <p><strong>FOR IMMEDIATE RELEASE:</strong><br /><br />

<strong>MAYOR LAUNCHES INAUGURAL KERRY BICYCLE FESTIVAL</strong> <br />
Tralee, Co.Kerry March 25th 2008 <br />
Kerry Bicycle Festival, an exciting new four day free festival celebrating the bicycle through film, art and music was launched today in Tralee by Mayor, Miriam McGillycuddy.  The festival will run  from Thursday April 17th to Sunday April 20th . Organisers of Kerry Bicycle Festival have announced a diverse line up of events which will take place in Tralee town and it's environs.  <br />
<br />
Kerry's very first bicycle festival will include events from bicycle clowns to bicycle films to bicycle jazz musicians.  The initiative was brought about by a group of volunteers including local arts group ClubHeadBangBang.  The festival aims to raise the profile of cycling culture by promoting cycling as an environmentally sustainable cost effective mode of transport and to highlight Kerry as a destination for cycle tourism.  
<br /><br />
An unique aspect of the festival will be a night of bicycle related short films. Submissions are currently being sought.  Films must be bicycle related and can be no longer than 15 minutes.  Submissions can be made in dvd format to Michael Kelly,  Tom Crean Business Centre, Kerry Technology Park, Tralee, County Kerry.  The deadline for entries is April 10th 2008.  Application forms and further information are available on the website:  www.kerrybicyclefestival.org.
<br /><br />
<strong>ABOUT KERRY BICYCLE FESTIVAL</strong><br />
Kerry Bicycle Festival is a four day free festival which brings together a selection of diverse events which aim to celebrate the bicycle. This festival encompasses musical, artistic and outdoor events all of which are free and will take place within the environs of Tralee town.  This festival was established in 2008 in order to raise the profile of cycling culture by making it more accessible and attractive and to promote cycling as an environmentally sustainable and cost effective mode of transport.  This is a voluntary organisation comprising a group of people  with diverse interests and backgrounds all with a common goal.<br /><br />

