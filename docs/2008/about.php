  <h2>About Kerry Bicycle Festival</h2>
  <p>
Kerry Bicycle Festival is a four day free festival which brings together a selection of diverse events which aim to celebrate the bicycle. This festival encompasses musical, artistic and outdoor events all of which are free and will take place within the environs of Tralee town. <br/><br/>This festival was established in 2008 in order to raise the profile of cycling culture by making it more accessible and attractive and to promote cycling as an environmentally sustainable and cost effective mode of transport. This is a voluntary organisation comprising a group of people with diverse interests and backgrounds all with a common goal.
